package com.toshiba.mwcloud.gs.tools.shell.annotation;

import java.lang.annotation.Documented;
import java.lang.annotation.ElementType;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;

/**
 * Indicate that an argument of a method can be {@code null}.
 *
 * <p>GridStore script engine treats an argument with this annotation as an optional argument. This
 * annotation does not have any attributes
 */
@Documented
@Retention(RetentionPolicy.RUNTIME)
@Target(ElementType.PARAMETER)
public @interface GSNullable {}
